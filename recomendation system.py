def main():
    category = choose_category()
    if not category:
        print("Invalid choice. Exiting.")
        return

    print("Please enter your preferences. You can enter genres, directors, artists, or specific titles.")
    preferences = input("Enter your preferences (separated by commas): ").split(",")
    recommended_items = recommend_items(category, preferences)
    if recommended_items:
        print("\nRecommended items based on your preferences:")
        for item in recommended_items:
            print(item)
    else:
        print("Sorry, no items match your preferences.")

