# Function to recommend items based on user preferences
def recommend_items(category, preferences):
    recommended_items = []
    for item, details in data.get(category, {}).items():
        if category != "travel_places" and (details.get("genre") in preferences or details.get("director") in preferences or item in preferences):
            recommended_items.append(item)
        elif category == "travel_places" and (details.get("type") in preferences or item in preferences):
            recommended_items.append(item)
    return recommended_items

# Function to prompt the user to choose a category
def choose_category():
    print("Welcome to the Recommendation System!")
    print("Please choose a category:")
    print("1. Books")
    print("2. Songs")
    print("3. Movies")
    print("4. Travel Places")
    choice = input("Enter your choice (1-4): ")
    categories = {"1": "books", "2": "songs", "3": "movies", "4": "travel_places"}
    return categories.get(choice)

